package com.mx.profuturo.bolsa.model.recruitment.response;

public class ResumenEntrevista {

	private String entrevistador;
	private String fecha;
	private String hora;
	private String sede;
	private String tipoEntrevista;
	private String idEntrevistador;
	
	public String getEntrevistador() {
		return entrevistador;
	}
	public void setEntrevistador(String entrevistador) {
		this.entrevistador = entrevistador;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getSede() {
		return sede;
	}
	public void setSede(String sede) {
		this.sede = sede;
	}
	public String getTipoEntrevista() {
		return tipoEntrevista;
	}
	public void setTipoEntrevista(String tipoEntrevista) {
		this.tipoEntrevista = tipoEntrevista;
	}
	public String getIdEntrevistador() {
		return idEntrevistador;
	}
	public void setIdEntrevistador(String idEntrevistador) {
		this.idEntrevistador = idEntrevistador;
	}
	
	
}
