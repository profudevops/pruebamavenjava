package com.mx.profuturo.bolsa.model.service.hiringform.dto;

public class AcceptDTO {
	
	private Integer idReclutamient;
	private String folio;
	
	private Integer numeroEmpleado;
	private String fecha;
	public Integer getIdReclutamient() {
		return idReclutamient;
	}
	public void setIdReclutamient(Integer idReclutamient) {
		this.idReclutamient = idReclutamient;
	}
	public String getFolio() {
		return folio;
	}
	public void setFolio(String folio) {
		this.folio = folio;
	}
	public Integer getNumeroEmpleado() {
		return numeroEmpleado;
	}
	public void setNumeroEmpleado(Integer numeroEmpleado) {
		this.numeroEmpleado = numeroEmpleado;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	

}
